package Desafios;

public class Desafio4Modificando {
	private String nome;
	private int idade;
	
	public Desafio4Modificando(String nome, int idade) {
		super();
		this.nome = nome;
		this.idade = idade;
	}

	public Desafio4Modificando() {
		super();
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getIdade() {
		return idade;
	}

	public void setIdade(int idade) {
		this.idade = idade;
	}
	
	
	
	

}
