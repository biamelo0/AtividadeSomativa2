package Desafios;

public class Produto {
	private String nome;

	public Produto(String nome) {
		super();
		this.nome = nome;
	}

	public Produto() {
		super();
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
	
	

}
